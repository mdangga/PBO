    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        String nim = NIM.getText();
        String nama = Nama.getText();
        String alamat = Alamat.getText();
        String jk = "";
        String phone = Phone.getText();
        String prodi = Prodi.getText();
        if (rbLaki.isSelected()) {
            jk = "Laki-Laki";
        } else if (rbPerempuan.isSelected()) {
            jk = "Perempuan";
        }

        String sql = "INSERT INTO user (NIM, Nama, Alamat, Jenis_Kelamin, Phone, Prodi) VALUES (?, ?, ?, ?, ?, ?)";

        try {
            Koneksi koneksi = new Koneksi();
            Connection conn = koneksi.connect();
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);

            stmt.setString(1, nim);
            stmt.setString(2, nama);
            stmt.setString(3, alamat);
            stmt.setString(4, jk);
            stmt.setString(5, phone);
            stmt.setString(6, prodi);

            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
            }
            tampilkanData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menambahkan data: " + e.getMessage());
        }
    }//GEN-LAST:event_SimpanActionPerformed

    private void UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UbahActionPerformed
        String nim = NIM.getText();
        String nama = Nama.getText();
        String jk = "";
        String phone = Phone.getText();
        String alamat = Alamat.getText();
        String prodi = Prodi.getText();
        if (rbLaki.isSelected()) {
            jk = "Laki-laki";
        } else if (rbPerempuan.isSelected()) {
            jk = "Perempuan";
        }
        String sql = "UPDATE user SET Nama = ?, Alamat = ?, Jenis_Kelamin = ?, Phone = ?, Prodi = ? WHERE nim = ?";

        try {
            Koneksi koneksi = new Koneksi();
            Connection conn = koneksi.connect();
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);

            stmt.setString(1, nama);
            stmt.setString(2, alamat);
            stmt.setString(3, jk);
            stmt.setString(4, phone);
            stmt.setString(5, prodi);
            stmt.setString(6, nim);

            int rowsUpdated = stmt.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
                tampilkanData();
            } else {
                JOptionPane.showMessageDialog(null, "NIM tidak ditemukan!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal mengubah data: " + e.getMessage());
        }
    }//GEN-LAST:event_UbahActionPerformed

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        String nim = NIM.getText();

        if (nim.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan NIM untuk menghapus data.");
            return;
        }
        String sql = "DELETE FROM user WHERE nim = ?";
        try {
            Koneksi koneksi = new Koneksi();
            Connection conn = koneksi.connect();
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, nim);
            int rowsDeleted = stmt.executeUpdate();
            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
                cleanTextbox();
                tampilkanData();
            } else {
                JOptionPane.showMessageDialog(null, "Data dengan NIM " + nim + " tidak ditemukan.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data: " + ex.getMessage());
        }
    }//GEN-LAST:event_HapusActionPerformed

