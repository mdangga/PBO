    public void tampilkanData() {

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NIM");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Jenis_Kelamin");
        model.addColumn("Phone");
        model.addColumn("Prodi");
        tabelMahasiswa.setModel(model);

        try {
            String sql = "SELECT * FROM user";
            Koneksi koneksi = new Koneksi();
            Connection conn = koneksi.connect();
            java.sql.Statement stmt = conn.createStatement();
            java.sql.ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                Object[] row = {
                    rs.getString("NIM"),
                    rs.getString("Nama"),
                    rs.getString("Alamat"),
                    rs.getString("Jenis_Kelamin"),
                    rs.getString("Phone"),
                    rs.getString("Prodi")
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan data: "+ e.getMessage());
        }
    }
    private void tabelMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {
        int baris = tabelMahasiswa.getSelectedRow();
        if (baris != -1) {
            String nim = tabelMahasiswa.getValueAt(baris, 0).toString();
            String nama = tabelMahasiswa.getValueAt(baris, 1).toString();
            String alamat = tabelMahasiswa.getValueAt(baris, 2).toString();
            String jk = tabelMahasiswa.getValueAt(baris, 3).toString();
            String phone = tabelMahasiswa.getValueAt(baris, 4).toString();
            String prodi = tabelMahasiswa.getValueAt(baris, 5).toString();
            
            NIM.setText(nim);
            Nama.setText(nama);
            if (jk.equals("Laki-Laki")) {
                rbLaki.setSelected(true);
                rbPerempuan.setSelected(false);

            } else if (jk.equals("Perempuan")) {
                rbLaki.setSelected(false);
                rbPerempuan.setSelected(true);
            }
            Phone.setText(phone);
            Alamat.setText(alamat);
            Prodi.setText(prodi);
        }
    }
    
